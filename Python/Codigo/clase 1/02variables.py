#variables en Python
edad= 25
#variable de tipo entero (int)
nombre= "Juan"
#variable de tipo cadena (str)
altura= 1.75
#variable de tipo flotante (float)
print("Mi nombre es", nombre, "y tengo", edad, "años.")
print("Mi altura es", altura, "metros.")
# Puedes realizar operaciones con variables
suma = edad + 5
print("En 5 años tendré", suma, "años.")
# También puedes usar variables para almacenar resultados de operaciones
multiplicacion = edad * 2
print("El doble de mi edad es", multiplicacion, "años.")
# Las variables pueden ser de diferentes tipos: enteros, flotantes, cadenas, etc.
# Es importante elegir nombres de variables descriptivos para mejorar la legibilidad del código
# Por ejemplo, en lugar de usar "x" como nombre de variable, es mejor usar "edad" o "nombre" para que sea más claro lo que representa la variable.

